#import "MITViewController.h"
#import "GMGridView.h"
#import "GMGridViewLayoutStrategies.h"
#import "MITIconCell.h"
#import "SMPageControl.h"
#import "Icon.h"
#import <QuartzCore/QuartzCore.h>
@interface MITViewController ()<GMGridViewDataSource, GMGridViewSortingDelegate, GMGridViewTransformationDelegate, GMGridViewActionDelegate>
@property(nonatomic,strong)GMGridView *gmGridView;
@property(nonatomic,strong)NSMutableArray *currentData;
@end

@implementation MITViewController

- (void)viewDidLoad
{
    [super viewDidLoad];
	// Do any additional setup after loading the view, typically from a nib.
    _currentData=[NSMutableArray array];
    for (int i=0; i<110; i++) {
        Icon *icon=[[Icon alloc] init];
        icon.imageURL=@"App-Store";
        icon.name=@"设置";
        [_currentData addObject:icon];
    }
    
    UIImageView *bg=[[UIImageView alloc] initWithImage:[UIImage imageNamed:@"background.jpg"]];
    [bg setFrame:self.view.bounds];
    [self.view addSubview:bg];
    GMGridView *gmGridView = [[GMGridView alloc] initWithFrame:self.view.bounds];
    gmGridView.autoresizingMask = UIViewAutoresizingFlexibleWidth | UIViewAutoresizingFlexibleHeight;
    gmGridView.backgroundColor = [UIColor clearColor];
    [self.view addSubview:gmGridView];
    _gmGridView = gmGridView;
    NSInteger spacing = INTERFACE_IS_PHONE ? 18 : 15;
    
    _gmGridView.style = GMGridViewStyleSwap;
    _gmGridView.itemSpacing = spacing;
    _gmGridView.minEdgeInsets = UIEdgeInsetsMake(spacing, spacing, spacing, spacing);
    _gmGridView.centerGrid = NO;
    _gmGridView.layoutStrategy=[GMGridViewLayoutStrategyFactory strategyFromType:GMGridViewLayoutHorizontalPagedLTR];
    _gmGridView.actionDelegate = self;
    _gmGridView.sortingDelegate = self;
    _gmGridView.transformDelegate = self;
    _gmGridView.dataSource = self;
    [_gmGridView setShowsHorizontalScrollIndicator:NO];

    SMPageControl *pageController=[[SMPageControl alloc] initWithFrame:CGRectMake(0, 0,320, 20)];
    [pageController setPageIndicatorImage:[UIImage imageNamed:@"pageDot"]];
	[pageController setCurrentPageIndicatorImage:[UIImage imageNamed:@"currentPageDot"]];
	[pageController setImage:[UIImage imageNamed:@"searchDot"] forPage:0];
    [pageController setNumberOfPages:10];
    [pageController setCenter:CGPointMake(self.view.bounds.size.width/2,self.view.bounds.size.height-67)];
    [self.view addSubview:pageController];

}


#pragma mark GMGridViewDataSource
//////////////////////////////////////////////////////////////

- (NSInteger)numberOfItemsInGMGridView:(GMGridView *)gridView
{
    return [_currentData count];
}

- (CGSize)GMGridView:(GMGridView *)gridView sizeForItemsInInterfaceOrientation:(UIInterfaceOrientation)orientation
{
    if (INTERFACE_IS_PHONE)
    {
        if (UIInterfaceOrientationIsLandscape(orientation))
        {
            return CGSizeMake(170, 135);
        }
        else
        {
            return CGSizeMake(57,72);
        }
    }
    else
    {
        if (UIInterfaceOrientationIsLandscape(orientation))
        {
            return CGSizeMake(285, 205);
        }
        else
        {
            return CGSizeMake(230, 175);
        }
    }
}

- (GMGridViewCell *)GMGridView:(GMGridView *)gridView cellForItemAtIndex:(NSInteger)index
{
    //NSLog(@"Creating view indx %d", index);
//    
//    CGSize size = [self GMGridView:gridView sizeForItemsInInterfaceOrientation:[[UIApplication sharedApplication] statusBarOrientation]];
    
    MITIconCell *cell = (MITIconCell*)[gridView dequeueReusableCell];
    
    if (!cell)
    {
        cell = [[MITIconCell alloc] initWithFrame:CGRectMake(0, 0,57, 57)];
        cell.deleteButtonIcon = [UIImage imageNamed:@"close_x.png"];
        cell.deleteButtonOffset = CGPointMake(-15, -15);
    
    }
    Icon *icon=[self.currentData objectAtIndex:index];
    cell.iconView.image=[UIImage imageNamed:icon.imageURL];
    cell.nameLabel.text=icon.name;

    return cell;
}


- (BOOL)GMGridView:(GMGridView *)gridView canDeleteItemAtIndex:(NSInteger)index
{
    return YES; //index % 2 == 0;
}

